package zyq.graduation.management.pojo;

/**
 * @author 周杨清
 * @date 2021/3/22 18:07
 */
public class ReturnClass<T> {
    private Integer code;
    private String msg;
    private Integer count;
    private T data;

    public ReturnClass(Integer code, String msg, Integer count, T data) {
        this.code = code;
        this.msg = msg;
        this.count = count;
        this.data = data;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
